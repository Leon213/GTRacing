package local.Pool_Variable;

/**
 *
 * @author Leonel
 */
public class Variables {
    private static int idUsuario;
    private static int userLevel;
    private static int currentEmploy;

    /**
     * @return the idUsuario
     */
    public static int getIdUsuario() {
        return idUsuario;
    }

    /**
     * @param aIdUsuario the idUsuario to set
     */
    public static void setIdUsuario(int aIdUsuario) {
        idUsuario = aIdUsuario;
    }

    /**
     * @return the userLevel
     */
    public static int getUserLevel() {
        return userLevel;
    }

    /**
     * @param aUserLevel the userLevel to set
     */
    public static void setUserLevel(int aUserLevel) {
        userLevel = aUserLevel;
    }

    /**
     * @return the currentEmploy
     */
    public static int getCurrentEmploy() {
        return currentEmploy;
    }

    /**
     * @param aCurrentEmploy the currentEmploy to set
     */
    public static void setCurrentEmploy(int aCurrentEmploy) {
        currentEmploy = aCurrentEmploy;
    }
}
