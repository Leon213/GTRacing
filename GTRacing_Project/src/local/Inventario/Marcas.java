    private void jbtnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnAceptarActionPerformed
        if(!jtfMarcas.getText().isEmpty() && jcbxPais.getSelectedIndex() >= 0){
            if(jrbtnAdd.isSelected()){
                int i;
                i = marDao.addMarca(jtfMarcas.getText(), jcbxPais.getSelectedItem().toString());
                
                if(i > 0){
                    Limpiar();
                    getRows();
                } else{
                    JOptionPane.showMessageDialog(null, "No se puede ingresar la marca");
                }
            } else if(jrbtnUpdate.isSelected()){
                int i;
                i = marDao.updateMarcas(jtfMarcas.getText(), jcbxPais.getSelectedItem().toString(), idMarca);
                
                if(i > 0){
                    Limpiar();
                    getRows();
                } else{
                    JOptionPane.showMessageDialog(null, "No se puede actualizar la marca");
                }
            } else if(jrbtnDelete.isSelected()){
                int i;
                i = marDao.deleteMarca(idMarca);
                
                if(i > 0){
                    Limpiar();
                    getRows();
                } else{
                    JOptionPane.showMessageDialog(null, "No se pudo elimiar la marca");
                }
            }
        } else{
            JOptionPane.showMessageDialog(null, "No puede dejar campos vacios");
        }
        
        idMarca = -1;
        marDao.close();
    }//GEN-LAST:event_jbtnAceptarActionPerformed

    private void jtblMarcasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtblMarcasMouseClicked
        if(jrbtnDelete.isSelected() || jrbtnUpdate.isSelected()){
            idMarca = (int) modeloMarcas.getValueAt(jtblMarcas.getSelectedRow(), 0);
            jtfMarcas.setText((String) modeloMarcas.getValueAt(jtblMarcas.getSelectedRow(), 1));
            String pa = (String) modeloMarcas.getValueAt(jtblMarcas.getSelectedRow(), 2);
            
            for (int i = 0; i < jcbxPais.getItemCount(); i++) {
                jcbxPais.setSelectedIndex(i);
                
                if(jcbxPais.getSelectedItem().toString().equals(pa)){
                    break;
                }
            }
        }
    }//GEN-LAST:event_jtblMarcasMouseClicked


