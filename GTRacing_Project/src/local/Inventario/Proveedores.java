    private void getRows() {
        ResultSet rs = probDao.getListProveedor();   //obteniendo los dados mediante una clase DAO
        try {
            Object[] myObject = new Object[4];

            while (rs.next()) {
                for (int i = 0; i < myObject.length; i++) {
                    myObject[i] = rs.getObject(i + 1);
                }
                ModeloProveedor.addRow(myObject);
            }

            rs.close();
            probDao.close();
        } catch (SQLException ex) {
            Logger.getLogger(Proveedores.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void limpieza() {
        jtfDireccion.setText("");
        jtfPbx.setText("");
        jtfProveedor.setText("");

        for (int i = 0; i < jtblProveedor.getRowCount(); i++) {
            ModeloProveedor.removeRow(i);
            i -= 1;
        }
    }
    
    //metodo para verificar los permisos para el formulario cargos
    private void accionRol(){
        Roles_DAO rolDao = new Roles_DAO();
        ResultSet rs = rolDao.listaTareas();
        int acciones;
        
        try {
            while(rs.next()){
                acciones = rs.getInt(1);
                switch(acciones){
                    case 13:
                        jrbtnAgregar.setEnabled(true);
                        break;
                        
                    case 14:
                        jrbtnEditar.setEnabled(true);
                        break;
                        
                    case 15:
                        jrbtnEliminar.setEnabled(true);
                        break;
                }
                
                if(acciones > 15)
                    break;
            }
        } catch (SQLException ex) {
            Logger.getLogger(Proveedores.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

