    private void getRows() {
        ResultSet rs = exDao.getListExisAuto();

        try {
            Object datos[] = new Object[6];

            while (rs.next()) {
                for (int i = 0; i < datos.length; i++) {
                    datos[i] = rs.getObject(i + 1);
                }
                modelAutoExistencia.addRow(datos);
            }

            rs.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error al cargar datos.");
            Logger.getLogger(Auto_Existencia.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void sDecimal(JTextField t){
        t.addKeyListener(new KeyAdapter() {
            public void keyTyped(KeyEvent e){
                char c = e.getKeyChar();
                if(((c < '0') || (c > '9')) && (c != KeyEvent.VK_BACK_SPACE) && (c != '.')){
                    getToolkit().beep();
                    e.consume();
                }
                if(c == '.' && t.getText().contains(".")){
                    e.consume();
                }
            }
        });
    }
    
    private void Limpiar() {
        jcbxAuto.setSelectedIndex(0);
        jcbxProveedor.setSelectedIndex(0);
        jtfCompra.setText("");
        jtfVenta.setText("");
        
        for (int i = 0; i < jtblAutoExistencia.getRowCount(); i++) {
            modelAutoExistencia.removeRow(i);
            i -= 1;
        }
    }
    
