    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(jcbxAuto.getSelectedIndex() >= 0 && jcbxProveedor.getSelectedIndex() >= 0 && !jtfCompra.getText().isEmpty() && !jtfVenta.getText().isEmpty()){
            if(jrbtnAdd.isSelected()){
                int i;
                ProveedoresLC pr = (ProveedoresLC) jcbxProveedor.getSelectedItem();
                AutosLC au = (AutosLC) jcbxAuto.getSelectedItem();
                int idau = au.getIdAuto(), idpr = pr.getIdProveedor();
                
                i = exDao.addExisAutos(idau, idpr, Double.parseDouble(jtfCompra.getText()), Double.parseDouble(jtfVenta.getText()));
                
                if(i > 0){
                    Limpiar();
                    getRows();
                } else{
                    JOptionPane.showMessageDialog(null, "No se logro completar la transaccion seleccionada");
                }
            } else if(jrbtnUpdate.isSelected()){
                int i;
                ProveedoresLC pr = (ProveedoresLC) jcbxProveedor.getSelectedItem();
                AutosLC au = (AutosLC) jcbxAuto.getSelectedItem();
                int idau = au.getIdAuto(), idpr = pr.getIdProveedor();
                
                i = exDao.updateExisAutos(idau, idpr, Double.parseDouble(jtfCompra.getText()), Double.parseDouble(jtfVenta.getText()), idExistencia);
                
                if(i > 0){
                    Limpiar();
                    getRows();
                } else{
                    JOptionPane.showMessageDialog(null, "No se logro completar la transaccion seleccionada");
                }
                idExistencia = -1;
            } else if(jrbtnDelete.isSelected()){
                int i;
                i = exDao.deleteExisAutos(idExistencia);
                
                if(i > 0){
                    Limpiar();
                    getRows();
                } else{
                    JOptionPane.showMessageDialog(null, "No se logro completar la transaccion seleccionada");
                }
                idExistencia = -1;
            } else{
                JOptionPane.showMessageDialog(null, "Debes de seleccionar una accion");
            }
            
            exDao.close();
        } else
            JOptionPane.showMessageDialog(null, "No debes dejar campos en blanco");
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jtblAutoExistenciaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtblAutoExistenciaMouseClicked
        if(jrbtnDelete.isSelected() || jrbtnUpdate.isSelected()){
            idExistencia = (int) modelAutoExistencia.getValueAt(jtblAutoExistencia.getSelectedRow(), 0);
            String auto = (String) modelAutoExistencia.getValueAt(jtblAutoExistencia.getSelectedRow(), 1);
            String prov = (String) modelAutoExistencia.getValueAt(jtblAutoExistencia.getSelectedRow(), 2);
            BigDecimal com = (BigDecimal) modelAutoExistencia.getValueAt(jtblAutoExistencia.getSelectedRow(), 3);
            BigDecimal ven = (BigDecimal) modelAutoExistencia.getValueAt(jtblAutoExistencia.getSelectedRow(), 4);
            
            jtfCompra.setText(com.toString());
            jtfVenta.setText(ven.toString());
            
            for (int i = 0; i < jcbxAuto.getItemCount(); i++) {
                jcbxAuto.setSelectedIndex(i);
                
                if(jcbxAuto.getSelectedItem().toString().equals(auto)){
                    break;
                }
            }
            
            for (int i = 0; i < jcbxProveedor.getItemCount(); i++) {
                jcbxProveedor.setSelectedIndex(i);
                
                if(jcbxProveedor.getSelectedItem().toString().equals(prov)){
                    break;
                }
            }
        }
    }//GEN-LAST:event_jtblAutoExistenciaMouseClicked


